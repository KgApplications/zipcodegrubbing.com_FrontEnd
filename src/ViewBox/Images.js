function images(value) {
    if (value === "Mexican") {
        return(
            "images/mexican.svg"
        )
    } 
    else if (value === "American") {
        return(
            "images/american.svg"
        )
    } 
    else if (value === "Vietnamese") {
        return(
            "images/vietnamese.jpg"
        )
    }
    else if (value === "Vietnamese") {
        return(
            "images/vietnamese.jpg"
        )
    }
    else if (value === "Latin American") {
        return(
            "images/enchiladas.jpg"
        )
    }
    else if (value === "Italian") {
        return(
            "images/pizza.jpg"
        )
    }
    else if (value === "Indian") {
        return(
            "images/indian.png"
        )
    } 
    else {
        return (
            "images/food.jpg"
        )
    }
}

export default images